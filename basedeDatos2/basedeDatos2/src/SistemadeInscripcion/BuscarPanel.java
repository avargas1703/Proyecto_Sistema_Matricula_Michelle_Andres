    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String criterio = comboCriterio.getSelectedItem().toString().toLowerCase();
        String valor = txtValorBusqueda.getText().trim();


        if (valor.isEmpty()) {
            lblMensaje.setText("Ingrese un valor para buscar.");
            lblMensaje.setForeground(java.awt.Color.RED);
            return;
        }


        modeloTabla.setRowCount(0);


        java.util.List<basededatos.BasedeDatos> resultados = gestorUsuarios.buscarUsuarios(criterio, valor);

        if (resultados.isEmpty()) {
            lblMensaje.setText("No se encontraron coincidencias.");
            lblMensaje.setForeground(java.awt.Color.RED);
        } else {
            for (basededatos.BasedeDatos usuario : resultados) {
                modeloTabla.addRow(new Object[]{
                    usuario.getCedula(),
                    usuario.getNombre(),
                    usuario.getApellido()
                });
            }
            lblMensaje.setText("Usuarios encontrados: " + resultados.size());
            lblMensaje.setForeground(java.awt.Color.BLUE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


